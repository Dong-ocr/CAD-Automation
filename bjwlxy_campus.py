##############################################################################
#  辅助函数
##############################################################################
def r(cx, cy, w, h, lyr):
    x1, y1 = cx - w/2, cy - h/2
    x2, y2 = cx + w/2, cy + h/2
    msp.add_lwpolyline([(x1,y1),(x2,y1),(x2,y2),(x1,y2),(x1,y1)], dxfattribs={"layer": lyr})

def t(txt, x, y, h, lyr, rot=0):
    te = msp.add_text(txt, height=h, dxfattribs={"layer": lyr})
    te.set_placement((x,y), align=AL)
    if rot: te.dxf.rotation = rot

def l(x1, y1, x2, y2, lyr):
    msp.add_line((x1,y1),(x2,y2), dxfattribs={"layer": lyr})

def c(cx, cy, rad, lyr):
    msp.add_circle((cx,cy), rad, dxfattribs={"layer": lyr})

def rx(x1, y1, x2, y2, lyr):
    msp.add_lwpolyline([(x1,y1),(x2,y1),(x2,y2),(x1,y2),(x1,y1)], dxfattribs={"layer": lyr})

